
vim.cmd [[
let mapleader = "\<space>"

nmap <leader>ve :edit ~/.config/nvim/init.lua<cr>
nmap <leader>vp :edit ~/.config/nvim/lua/plugins.lua<cr>
nmap <leader>vk :edit ~/.config/nvim/lua/keymaps.lua<cr>
nmap <leader>vo :edit ~/.config/nvim/lua/options.lua<cr>

nmap <leader>vc :edit ~/.config/nvim/coc-settings.json<cr>
nmap <leader>vr :luafile ~/.config/nvim/init.lua<cr>

nmap <leader>k :nohlsearch<CR>
nmap <leader>Q :bufdo bdelete<cr>

" Allow gf to open non-existent files
map gf :edit <cfile><cr>

" Reselect visual selection after indenting
vnoremap < <gv
vnoremap > >gv

" Maintain the cursor position when yanking a visual selection
" http://ddrscott.github.io/blog/2016/yank-without-jank/
vnoremap y myy`y
vnoremap Y myY`y

" When text is wrapped, move by terminal rows, not lines, unless a count is provided
noremap <silent> <expr> j (v:count == 0 ? 'gj' : 'j')
noremap <silent> <expr> k (v:count == 0 ? 'gk' : 'k')

" Paste replace visual selection without copying it
vnoremap <leader>p "_dP

" Make Y behave like the other capitals
nnoremap Y y$

" Keep it centered
nnoremap n nzzzv
nnoremap N Nzzzv
nnoremap J mzJ`z

" Open the current file in the default program
nmap <leader>x :!xdg-open %<cr><cr>

" Quicky escape to normal mode
imap jj <esc>

" Easy insertion of a trailing ; or , from insert mode
imap ;; <Esc>A;<Esc>
imap ,, <Esc>A,<Esc>

cmap w!! %!sudo tee > /dev/null %

]]
